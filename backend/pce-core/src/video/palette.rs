use pce_config::PcePaletteType;
use std::array;

pub type PcePalette = [(u8, u8, u8); 512];

pub fn for_type(palette: PcePaletteType) -> &'static PcePalette {
    match palette {
        PcePaletteType::PceComposite => COMPOSITE_PALETTE,
        PcePaletteType::Linear => LINEAR_PALETTE,
    }
}

pub fn make_greyscale(palette: &PcePalette) -> PcePalette {
    array::from_fn(|i| {
        let (r, g, b) = palette[i];
        let r: f64 = r.into();
        let g: f64 = g.into();
        let b: f64 = b.into();

        // Remove chroma information by converting from RGB to YUV, setting U and V to 0, then converting back to RGB
        // This is equivalent to replacing all color components with Y
        let y = 0.299 * r + 0.587 * g + 0.114 * b;
        let y = y.round() as u8;

        (y, y, y)
    })
}

